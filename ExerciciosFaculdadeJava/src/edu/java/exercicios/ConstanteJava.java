package edu.java.exercicios;

public class ConstanteJava {

	public static void main(String[] args) {
		
		//CONSTANTE EM JAVA SÃO VARIAVEIS COM TODAS AS LETRAS MAIUSCULAS
		
		final int UP = 0;
		final int DOWN = 1;
		System.out.println(UP); 
		System.out.println(DOWN); 
				
		//Floating Constante         
		final float PI = 3.14f;
		System.out.println(PI);
				
		//Boolean Constante         
		final boolean RUN = true;  
		System.out.println(RUN);  
				
		//Char Constante         
		final char LETRA_A = 'A';
				
		//String Constante         
		final String CONST = "love for forever";
		System.out.println(CONST);
	}

}
